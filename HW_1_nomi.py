n = int(first_multiple_input[0])
m = int(first_multiple_input[1])
matrix = []
for _ in range(n):
    matrix_item = input()
    matrix.append(matrix_item)
nuovo = []
for colonna in range(m):
    lista = ""
    for riga in range(n):
        lista += matrix[riga][colonna]
    nuovo.append(lista)
lett = ""
for i in nuovo:
    lett += str(i)
lett = re.sub(r"(?<=\w)[^a-zA-Z0-9]+(?=\w)", " " , lett )
print(lett)
